package julend;


public class JsonDBName {


    private String from;
    private String fromdb;
    private String fromtb;
    private String hivedb;
    private String hivetb;
    private String updatetype;
    private String updateby;


    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getFromdb() {
        return fromdb;
    }

    public void setFromdb(String fromdb) {
        this.fromdb = fromdb;
    }

    public String getFromtb() {
        return fromtb;
    }

    public void setFromtb(String fromtb) {
        this.fromtb = fromtb;
    }

    public String getHivedb() {
        return hivedb;
    }

    public void setHivedb(String hivedb) {
        this.hivedb = hivedb;
    }

    public String getHivetb() {
        return hivetb;
    }

    public void setHivetb(String hivetb) {
        this.hivetb = hivetb;
    }

    public String getUpdatetype() {
        return updatetype;
    }

    public void setUpdatetype(String updatetype) {
        this.updatetype = updatetype;
    }

    public String getUpdateby() {
        return updateby;
    }

    public void setUpdateby(String updateby) {
        this.updateby = updateby;
    }



}
